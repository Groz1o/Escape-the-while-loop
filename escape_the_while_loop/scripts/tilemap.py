    def load_map(self):
        for y, row in enumerate(self.map):
            for x, col in enumerate(row):
                if self.map[y][x] == 1:
                    self.collidable_tiles[f"{x};{y}"] = [x, y]
                elif self.map[y][x] == 5:
                    self.spawners[f"{x};{y}"] = ["player", [x, y]]
                elif self.map[y][x] == 2:
                    self.spawners[f"{x};{y}"] = ["squre_bracket_enemy", [x, y]]
                elif self.map[y][x] == 3:
                    self.spawners[f"{x};{y}"] = ["curly_bracket_enemy", [x, y]]
                elif self.map[y][x] == 4:
                    self.spawners[f"{x};{y}"] = ["normal_bracket_enemy", [x, y]]

    def render(self, display, offset):
        for tile_pos in self.collidable_tiles.values():
            display.blit(self.game.assets["tile"], (tile_pos[0] * self.tile_size - offset[0], tile_pos[1] * self.tile_size - offset[1]))
